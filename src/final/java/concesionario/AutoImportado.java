package concesionario;

public class AutoImportado {
	
	private String marca;
	private String modelo;
	private String añoFabricacion;
	private String placa;
	private String kilometraje;
	private String codigoImportacion;
	private String valorImportacion;
	
	
	public String getMarca() {
		return marca;
	}
	public void setMarca(String marca) {
		this.marca = marca;
	}
	public String getModelo() {
		return modelo;
	}
	public void setModelo(String modelo) {
		this.modelo = modelo;
	}
	public String getPlaca() {
		return placa;
	}
	public void setPlaca(String placa) {
		this.placa = placa;
	}
	public String getAñoFabricacion() {
		return añoFabricacion;
	}
	public void setAñoFabricacion(String añoFabricacion) {
		this.añoFabricacion = añoFabricacion;
	}
	public String getKilometraje() {
		return kilometraje;
	}
	public void setKilometraje(String kilometraje) {
		this.kilometraje = kilometraje;
	}
	public String getCodigoImportacion() {
		return codigoImportacion;
	}
	public void setCodigoImportacion(String codigoImportacion) {
		this.codigoImportacion = codigoImportacion;
	}
	public String getValorImportacion() {
		return valorImportacion;
	}
	public void setValorImportacion(String valorImportacion) {
		this.valorImportacion = valorImportacion;
	}

}
